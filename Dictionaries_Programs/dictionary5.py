def dictionary5():
    # using dict() method and updating , adding
    phone_no = dict({
        'sam': 1234567890,
        'ram': 9876543212,
    })
    print(phone_no)
    # adding key value pairs
    phone_no['rahim'] = 4455664423
    print(phone_no)
    # updating rahim's number
    phone_no['rahim'] = 1234497976
    print(phone_no)


dictionary5()
